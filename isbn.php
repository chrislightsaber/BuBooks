<b>What is an ISBN number?</b>
<p>An ISBN (International Standard Book Number) is a unique <b>10</b> or <b>13</b> digit product number 
which is specific to a particular title, edition and publisher. Searching by ISBN makes it easy to 
find the EXACT book you need.</p>

<b>Where is the ISBN number?</b>
<p>ISBN numbers are usually found on the back cover of a textbook next to the bar code.</p> 
